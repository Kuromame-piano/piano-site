def expand(template: str, name: str) -> str:
    return template.replace("{{name}}", name).replace("{{area}}", name)


def render_faqs_ts(faqs: list, indent: str = "        ") -> str:
    """FAQ リストを TypeScript の配列リテラルに変換する。
    バリデータが期待する形式:
      faqs: [\n        { question: "...", answer: "..." },\n        ...],\n        uniqueContent:
    """
    inner = indent + "    "
    lines = ["["]
    for faq in faqs:
        q = faq["question"].replace("\\", "\\\\").replace('"', '\\"')
        a = faq["answer"].replace("\\", "\\\\").replace('"', '\\"')
        lines.append(f'{inner}{{ question: "{q}", answer: "{a}" }},')
    lines.append(f"{indent}]")
    return "\n".join(lines)


def build_area_map(text: str) -> dict:
    """slug → (name, groupId) のマップを返す。"""
    area_map = {}
    pattern = re.compile(
        r'slug:\s*"([^"]+)",\s*\n\s*name:\s*"([^"]+)",\s*\n\s*groupId:\s*"([^"]+)"'
    )
    for m in pattern.finditer(text):
        area_map[m.group(1)] = (m.group(2), m.group(3))
    return area_map


def slug_positions(text: str, area_map: dict) -> list:
    """(position_after_slug_value, slug) のリストを位置順で返す。"""
    result = []
    for m in re.finditer(r'slug:\s*"([^"]+)"', text):
        slug = m.group(1)
        if slug in area_map:
            result.append((m.end(), slug))
    result.sort(key=lambda x: x[0])
    return result


def find_slug_for_position(pos: int, positions: list) -> str | None:
    """pos より前に現れる直近の slug を返す。"""
    current = None
    for (end_pos, slug) in positions:
        if end_pos <= pos:
            current = slug
        else:
            break
    return current


def replace_faqs(text: str, area_map: dict, positions: list) -> str:
    """faqs: commonFaqs, をグループ別インライン FAQ に置換する。"""
    result_parts = []
    last = 0
    pattern = re.compile(r'faqs:\s*commonFaqs,')
    for m in pattern.finditer(text):
        slug = find_slug_for_position(m.start(), positions)
        if slug and slug in area_map:
            name, group_id = area_map[slug]
            templates = GROUP_FAQS.get(group_id)
            if not templates:
                print(f"WARNING: no FAQ template for group '{group_id}' (slug={slug})")
                continue
            faqs = [
                {"question": expand(f["question"], name), "answer": expand(f["answer"], name)}
                for f in templates
            ]
            faq_ts = render_faqs_ts(faqs)
            replacement = f"faqs: {faq_ts},"
        else:
            replacement = m.group(0)
        result_parts.append(text[last:m.start()])
        result_parts.append(replacement)
        last = m.end()
    result_parts.append(text[last:])
    return "".join(result_parts)


def replace_instructor_intros(text: str, area_map: dict, positions: list) -> str:
    """instructorIntroduction: "..." をグループ別テキストに置換する。"""
    result_parts = []
    last = 0
    # 現在の値はダブルクォート文字列（エスケープなし）
    pattern = re.compile(r'instructorIntroduction:\s*"([^"]*)"')
    for m in pattern.finditer(text):
        slug = find_slug_for_position(m.start(), positions)
        if slug and slug in area_map:
            name, group_id = area_map[slug]
            template = GROUP_INSTRUCTOR_INTROS.get(group_id)
            if not template:
                print(f"WARNING: no instructor intro template for group '{group_id}' (slug={slug})")
                replacement = m.group(0)
            else:
                value = expand(template, name).replace('"', '\\"')
                replacement = f'instructorIntroduction: "{value}"'
        else:
            replacement = m.group(0)
        result_parts.append(text[last:m.start()])
        result_parts.append(replacement)
        last = m.end()
    result_parts.append(text[last:])
    return "".join(result_parts)


def replace_cta_subcopy(text: str, area_map: dict, positions: list) -> str:
    """cta: { subcopy: "..." } の subcopy をグループ別テキストに置換する。"""
    result_parts = []
    last = 0
    pattern = re.compile(r'(cta:\s*\{\s*subcopy:\s*)"([^"]*)"(\s*\})')
    for m in pattern.finditer(text):
        slug = find_slug_for_position(m.start(), positions)
        if slug and slug in area_map:
            name, group_id = area_map[slug]
            template = GROUP_CTA_SUBCOPY.get(group_id)
            if not template:
                replacement = m.group(0)
            else:
                value = expand(template, name).replace('"', '\\"')
                replacement = f'{m.group(1)}"{value}"{m.group(3)}'
        else:
            replacement = m.group(0)
        result_parts.append(text[last:m.start()])
        result_parts.append(replacement)
        last = m.end()
    result_parts.append(text[last:])
    return "".join(result_parts)


def main():
    with open(INPUT_PATH, "r", encoding="utf-8") as f:
        text = f.read()

    area_map = build_area_map(text)
    print(f"Detected {len(area_map)} areas: {', '.join(sorted(area_map))}")

    pos_list = slug_positions(text, area_map)

    print("Replacing FAQs...")
    text = replace_faqs(text, area_map, pos_list)

    # slug positions must be recalculated after FAQ replacement (offsets shift)
    pos_list = slug_positions(text, area_map)

    print("Replacing instructor intros...")
    text = replace_instructor_intros(text, area_map, pos_list)

    pos_list = slug_positions(text, area_map)

    print("Replacing CTA subcopy...")
    text = replace_cta_subcopy(text, area_map, pos_list)

    with open(OUTPUT_PATH, "w", encoding="utf-8") as f:
        f.write(text)

    print(f"Done. Written to {OUTPUT_PATH}")


if __name__ == "__main__":
    main()
